/*
Write a Dart program to perform and print the following operations on two numbers a and b:

    Sum
    Difference
    Product
    Quotient

Assign a = 10 and b = 5.
*/

void main () {
  //values assigned 
  int a = 10;
  int b = 5;

  int sum = a+b;
  int diff = a-b;
  int mul = a*b;
  double div = a/b;

  //printing results
  print("sum: $sum");
  print("Diffrence: $diff");
  print("Product: $mul");
  print("Quotient: $div");

}